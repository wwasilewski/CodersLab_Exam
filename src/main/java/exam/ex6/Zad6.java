//Uzupełnij ciało metody tak aby zwracała napis powstały z zamiany w napisie `str`
// wszystkich wystąpień napisu `forReplace` przez napis `replacement`.

package exam.ex6;

public class Zad6 {

    public String replaceStr(String str, String forReplace, String replacement) {
        return str.replace(forReplace, replacement);
    }
}
